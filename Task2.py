#math module is imported to use the mathematical functions
import math 
num=int(input("Enter a number: "))
# square root, natural logarithm and sine of a number is calculated using the math module
sqrt=math.sqrt(num)
print("The square root of",num,"is",sqrt)
log=math.log(num)
print("The natural logarithm of",num,"is",log)  
sine=math.sin(num)  
print("The sine of",num,"is",sine)  
